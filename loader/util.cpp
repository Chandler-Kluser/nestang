size_t formatPacket(byte* buf, int address, const void* data, size_t data_size) {
	byte* org = buf;
	while (data_size) {
		int n = data_size > 256 ? 256 : (int)data_size;
		int cksum = address + n;
		buf[1] = address;
		buf[2] = (byte)n;
		for (int i = 0; i < n; i++) {
			int v = ((byte*)data)[i];
			buf[i + 3] = v;
			cksum += v;
		}
		buf[0] = -cksum;
		buf += n + 3;
		data = (char*)data + n;
		data_size -= n;
	}
	return buf - org;
}

